// Build translations from the compact format
const langOrder: Language[] = ["en", "hi", "ta", "te", "kn", "ml"];
const translations: Record<Language, Record<string, string>> = { en: {}, hi: {}, ta: {}, te: {}, kn: {}, ml: {} };

for (const [key, values] of Object.entries(baseKeys)) {
  const vals = values as string[];
  langOrder.forEach((lang, i) => {
    if (vals[i]) translations[lang][key] = vals[i];
  });
}

interface I18nContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
}

const I18nContext = createContext<I18nContextType>({
  language: "en",
  setLanguage: () => {},
  t: (key: string) => key,
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const { user } = useAuth();
  const [language, setLanguageState] = useState<Language>(() => {
    return (localStorage.getItem("language") as Language) || "en";
  });

  // Load from profile on login
  useEffect(() => {
    if (!user) return;
    supabase
      .from("profiles")
      .select("language_preference")
      .eq("user_id", user.id)
      .maybeSingle()
      .then(({ data }) => {
        if (data?.language_preference) {
          const lang = data.language_preference as Language;
          setLanguageState(lang);
          localStorage.setItem("language", lang);
        }
      });
  }, [user]);

  const setLanguage = async (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem("language", lang);
    if (user) {
      await supabase
        .from("profiles")
        .update({ language_preference: lang })
        .eq("user_id", user.id);
    }
  };

  const t = (key: string): string => {
    return translations[language]?.[key] || translations.en[key] || key;
  };

  return (
    <I18nContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
